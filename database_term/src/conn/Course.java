package conn;

public class Course {
	private String c_id;
	private int c_id_no;
	private String c_name;
	private String c_major;
	private int c_unit;
	private int c_grade;
	private int c_semester;
	private String c_day;
	private int c_time;
	private int c_max;

	public String getC_id() {
		return c_id;
	}
	public void setC_id(String c_id) {
		this.c_id = c_id;
	}
	public int getC_id_no() {
		return c_id_no;
	}
	public void setC_id_no(int c_id_no) {
		this.c_id_no = c_id_no;
	}
	public String getC_name() {
		return c_name;
	}
	public void setC_name(String c_name) {
		this.c_name = c_name;
	}
	public String getC_major() {
		return c_major;
	}
	public void setC_major(String c_major) {
		this.c_major = c_major;
	}
	public int getC_unit() {
		return c_unit;
	}
	public void setC_unit(int c_unit) {
		this.c_unit = c_unit;
	}
	public int getC_grade() {
		return c_grade;
	}
	public void setC_grade(int c_grade) {
		this.c_grade = c_grade;
	}
	public int getC_semester() {
		return c_semester;
	}
	public void setC_semester(int c_semester) {
		this.c_semester = c_semester;
	}
	public String getC_day() {
		return c_day;
	}
	public void setC_day(String c_day) {
		this.c_day = c_day;
	}
	public int getC_time() {
		return c_time;
	}
	public void setC_time(int c_time) {
		this.c_time = c_time;
	}
	public int getC_max() {
		return c_max;
	}
	public void setC_max(int c_max) {
		this.c_max = c_max;
	}
}
